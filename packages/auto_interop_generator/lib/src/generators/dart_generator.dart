  String _buildParameterList(List<UtsParameter> params) {
    final positional = params.where((p) => !p.isNamed).toList();
    final named = params.where((p) => p.isNamed).toList();

    final parts = <String>[];

    for (final p in positional) {
      parts.add('${p.type.toDartType()} ${_esc(p.name)}');
    }

    if (named.isNotEmpty) {
      final namedParts = <String>[];
      for (final p in named) {
        final dartType = p.type.toDartType();
        if (p.isOptional) {
          // dynamic is already nullable; types already ending with '?' don't need another
          final suffix =
              (dartType == 'dynamic' || dartType.endsWith('?')) ? '' : '?';
          namedParts.add('$dartType$suffix ${_esc(p.name)}');
        } else {
          namedParts.add('required $dartType ${_esc(p.name)}');
        }
      }
      parts.add('{${namedParts.join(', ')}}');
    }

    return parts.join(', ');
  }

  String _serializeParam(UnifiedTypeSchema schema, UtsParameter param,
      {bool nullChecked = false}) {
    return _serializeExpr(schema, param.type, _esc(param.name),
        nullChecked: nullChecked);
  }

  /// Returns the expression to serialize a value for sending over the channel.
  ///
  /// [depth] tracks nesting level for unique variable names in nested
  /// collections (e.g., `e0`, `e1` instead of shadowed `e`).
  /// [nullChecked] indicates the value has already been null-checked by the
  /// caller, so nullable operators should not be emitted.
  String _serializeExpr(UnifiedTypeSchema schema, UtsType type, String expr,
      {int depth = 0, bool nullChecked = false}) {
    final isNullable = type.nullable && !nullChecked;
    final dot = isNullable ? '?.' : '.';
    if (type.kind == UtsTypeKind.primitive && type.name == 'DateTime') {
      return '$expr${dot}toIso8601String()';
    }
    if (type.kind == UtsTypeKind.primitive && type.name == 'Duration') {
      return '$expr${dot}inMicroseconds';
    }
    if (type.kind == UtsTypeKind.primitive && type.name == 'Uri') {
      return '$expr${dot}toString()';
    }
    if (type.kind == UtsTypeKind.nativeObject) {
      return '$expr${isNullable ? '?' : ''}._handle';
    }
    if (type.kind == UtsTypeKind.object) {
      if (_isReferenceTypeClass(schema, type.name)) {
        return '$expr${isNullable ? '?' : ''}._handle';
      }
      // Only call toMap() on known data classes (defined in schema.types)
      if (schema.types.any((t) => t.name == type.name)) {
        return '$expr${dot}toMap()';
      }
      return expr;
    }
    if (type.kind == UtsTypeKind.enumType) {
      return '$expr.name';
    }
    if (type.kind == UtsTypeKind.callback) {
      return 'CallbackManager.instance.register($expr)';
    }
    if (type.kind == UtsTypeKind.list) {
      final elementType = type.typeArguments?.first;
      if (elementType != null && _needsTypeSerialization(elementType)) {
        final eVar = 'e$depth';
        return '$expr.map(($eVar) => ${_serializeExpr(schema, elementType, eVar, depth: depth + 1)}).toList()';
      }
    }
    if (type.kind == UtsTypeKind.map) {
      final keyType = type.typeArguments?.first;
      final valueType =
          type.typeArguments != null && type.typeArguments!.length > 1
              ? type.typeArguments![1]
              : null;
      final keyNeedsSer = keyType != null && _needsTypeSerialization(keyType);
      final valueNeedsSer =
          valueType != null && _needsTypeSerialization(valueType);
      if (keyNeedsSer || valueNeedsSer) {
        final kVar = 'k$depth';
        final vVar = 'v$depth';
        final keySer = keyNeedsSer
            ? _serializeExpr(schema, keyType, kVar, depth: depth + 1)
            : kVar;
        final valueSer = valueNeedsSer
            ? _serializeExpr(schema, valueType, vVar, depth: depth + 1)
            : vVar;
        return '$expr.map(($kVar, $vVar) => MapEntry($keySer, $valueSer))';
      }
    }
    return expr;
  }

  /// Whether a type needs serialization when sending over the channel.
  bool _needsTypeSerialization(UtsType type) {
    if (type.kind == UtsTypeKind.primitive && type.name == 'DateTime') {
      return true;
    }
    if (type.kind == UtsTypeKind.primitive && type.name == 'Duration') {
      return true;
    }
    if (type.kind == UtsTypeKind.primitive && type.name == 'Uri') {
      return true;
    }
    if (type.kind == UtsTypeKind.object) return true;
    if (type.kind == UtsTypeKind.nativeObject) return true;
    if (type.kind == UtsTypeKind.enumType) return true;
    if (type.kind == UtsTypeKind.callback) return true;
    if (type.kind == UtsTypeKind.list) {
      final elementType = type.typeArguments?.first;
      return elementType != null && _needsTypeSerialization(elementType);
    }
    if (type.kind == UtsTypeKind.map) {
      final keyType = type.typeArguments?.first;
      final valueType =
          type.typeArguments != null && type.typeArguments!.length > 1
              ? type.typeArguments![1]
              : null;
      return (keyType != null && _needsTypeSerialization(keyType)) ||
          (valueType != null && _needsTypeSerialization(valueType));
    }
    return false;
  }

  String _serializeField(UnifiedTypeSchema schema, UtsField field,
      {bool nullChecked = false}) {
    final eName = _esc(field.name);
    if (!field.nullable || nullChecked) {
      return _serializeExpr(schema, field.type, eName, nullChecked: true);
    }
    // Nullable: need ?. operator
    final type = field.type;
    if (type.kind == UtsTypeKind.primitive && type.name == 'DateTime') {
      return '$eName?.toIso8601String()';
    }
    if (type.kind == UtsTypeKind.primitive && type.name == 'Duration') {
      return '$eName?.inMicroseconds';
    }
    if (type.kind == UtsTypeKind.primitive && type.name == 'Uri') {
      return '$eName?.toString()';
    }
    if (type.kind == UtsTypeKind.nativeObject) {
      return '$eName?._handle';
    }
    if (type.kind == UtsTypeKind.object) {
      if (_isReferenceTypeClass(schema, type.name)) {
        return '$eName?._handle';
      }
      // Only call toMap() on known data classes (defined in schema.types)
      if (schema.types.any((t) => t.name == type.name)) {
        return '$eName?.toMap()';
      }
      return eName;
    }
    if (type.kind == UtsTypeKind.enumType) {
      return '$eName?.name';
    }
    if (type.kind == UtsTypeKind.list) {
      final elementType = type.typeArguments?.first;
      if (elementType != null && _needsTypeSerialization(elementType)) {
        return '$eName?.map((e0) => ${_serializeExpr(schema, elementType, 'e0', depth: 1)}).toList()';
      }
    }
    if (type.kind == UtsTypeKind.map) {
      final keyType = type.typeArguments?.first;
      final valueType =
          type.typeArguments != null && type.typeArguments!.length > 1
              ? type.typeArguments![1]
              : null;
      final keyNeedsSer = keyType != null && _needsTypeSerialization(keyType);
      final valueNeedsSer =
          valueType != null && _needsTypeSerialization(valueType);
      if (keyNeedsSer || valueNeedsSer) {
        final keySer = keyNeedsSer
            ? _serializeExpr(schema, keyType, 'k0', depth: 1)
            : 'k0';
        final valueSer = valueNeedsSer
            ? _serializeExpr(schema, valueType, 'v0', depth: 1)
            : 'v0';
        return '$eName?.map((k0, v0) => MapEntry($keySer, $valueSer))';
      }
    }
    return eName;
  }

  String _castFromMap(UnifiedTypeSchema schema, UtsField field) {
    final key = "'${field.name}'";
    final type = field.type;
    final nullable = field.nullable;

    if (type.kind == UtsTypeKind.primitive) {
      if (type.name == 'DateTime') {
        if (nullable) {
          return 'map[$key] != null ? DateTime.parse(map[$key] as String) : null';
        }
        return 'DateTime.parse(map[$key] as String)';
      }
      if (type.name == 'Duration') {
        if (nullable) {
          return 'map[$key] != null ? Duration(microseconds: map[$key] as int) : null';
        }
        return 'Duration(microseconds: map[$key] as int)';
      }
      if (type.name == 'Uri') {
        if (nullable) {
          return 'map[$key] != null ? Uri.parse(map[$key] as String) : null';
        }
        return 'Uri.parse(map[$key] as String)';
      }
      if (nullable) {
        return 'map[$key] as ${type.toDartType()}?';
      }
      return 'map[$key] as ${type.toDartType()}';
    }

    if (type.kind == UtsTypeKind.nativeObject) {
      if (nullable) {
        return 'map[$key] != null ? ${type.name}.fromHandle(map[$key] as String) : null';
      }
      return '${type.name}.fromHandle(map[$key] as String)';
    }

    if (type.kind == UtsTypeKind.object) {
      if (_isReferenceTypeClass(schema, type.name)) {
        if (nullable) {
          return 'map[$key] != null ? ${type.name}.fromHandle(map[$key] as String) : null';
        }
        return '${type.name}.fromHandle(map[$key] as String)';
      }
      if (!_isEmptyClassWithoutFromMap(schema, type.name)) {
        if (nullable) {
          return 'map[$key] != null ? ${type.name}.fromMap(map[$key] as Map<String, dynamic>) : null';
        }
        return '${type.name}.fromMap(map[$key] as Map<String, dynamic>)';
      }
      // Opaque/empty type — pass through as dynamic cast
      if (nullable) {
        return 'map[$key] as ${type.name}?';
      }
      return 'map[$key] as ${type.name}';
    }

    if (type.kind == UtsTypeKind.enumType) {
      if (nullable) {
        return 'map[$key] != null ? ${type.name}.values.byName(map[$key] as String) : null';
      }
      return '${type.name}.values.byName(map[$key] as String)';
    }

    if (type.kind == UtsTypeKind.list) {
      final elementType = type.typeArguments?.first;
      if (elementType != null && _needsDeserialization(schema, elementType)) {
        final elementDeser =
            _deserializeListElement(schema, elementType, 'e0', depth: 1);
        if (nullable) {
          return 'map[$key] != null ? (map[$key] as List).map((e0) => $elementDeser).toList() : null';
        }
        return '(map[$key] as List).map((e0) => $elementDeser).toList()';
      }
      // Platform channels return List<dynamic>; use .cast<T>() for typed lists
      final elementDartType = elementType?.toDartType() ?? 'dynamic';
      if (nullable) {
        return "map[$key] != null ? (map[$key] as List).cast<$elementDartType>() : null";
      }
      return '(map[$key] as List).cast<$elementDartType>()';
    }

    if (type.kind == UtsTypeKind.map) {
      final keyType = type.typeArguments?.first;
      final valueType =
          type.typeArguments != null && type.typeArguments!.length > 1
              ? type.typeArguments![1]
              : null;
      final keyNeedsDeser =
          keyType != null && _needsDeserialization(schema, keyType);
      final valueNeedsDeser =
          valueType != null && _needsDeserialization(schema, valueType);
      if (keyNeedsDeser || valueNeedsDeser) {
        final keyDartType = keyType?.toDartType() ?? 'String';
        final keyDeser = keyNeedsDeser
            ? _deserializeListElement(schema, keyType, 'k0', depth: 1)
            : 'k0 as $keyDartType';
        final valueDeser = valueNeedsDeser
            ? _deserializeListElement(schema, valueType, 'v0', depth: 1)
            : 'v0';
        if (nullable) {
          return 'map[$key] != null ? (map[$key] as Map).map((k0, v0) => MapEntry($keyDeser, $valueDeser)) : null';
        }
        return '(map[$key] as Map).map((k0, v0) => MapEntry($keyDeser, $valueDeser))';
      }
      // Platform channels return Map<Object?, Object?>; use .cast<K,V>()
      final keyDartType = keyType?.toDartType() ?? 'String';
      final valueDartType = valueType?.toDartType() ?? 'dynamic';
      if (nullable) {
        return "map[$key] != null ? (map[$key] as Map).cast<$keyDartType, $valueDartType>() : null";
      }
      return '(map[$key] as Map).cast<$keyDartType, $valueDartType>()';
    }

    if (nullable) {
      return 'map[$key] as ${type.toDartType()}?';
    }
    return 'map[$key] as ${type.toDartType()}';
  }

  /// Names from dart:core that must never be stubbed as opaque handles.
  static const _dartCoreNames = {
    'Object',
    'Function',
    'Type',
    'Symbol',
    'Null',
