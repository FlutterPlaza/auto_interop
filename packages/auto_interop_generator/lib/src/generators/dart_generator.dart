  /// Collects all nativeObject type names referenced in the schema
  /// that are NOT defined in `definedTypeNames`.
  Set<String> _collectExternalNativeObjectTypes(UnifiedTypeSchema schema) {
    final defined = schema.definedTypeNames;
    final external = <String>{};

    void walkType(UtsType type) {
      if (type.kind == UtsTypeKind.nativeObject &&
          type.name != 'dynamic' &&
          !defined.contains(type.name) &&
          !_dartCoreNames.contains(type.name)) {
        external.add(type.name);
      }
      type.typeArguments?.forEach(walkType);
      type.parameterTypes?.forEach(walkType);
      if (type.returnType != null) walkType(type.returnType!);
    }

    for (final cls in [...schema.classes, ...schema.types]) {
      for (final field in cls.fields) {
        walkType(field.type);
      }
      for (final method in cls.methods) {
        walkType(method.returnType);
        for (final param in method.parameters) {
          walkType(param.type);
        }
      }
      if (cls.constructorParameters != null) {
        for (final param in cls.constructorParameters!) {
          walkType(param.type);
        }
      }
    }
    for (final func in schema.functions) {
      walkType(func.returnType);
      for (final param in func.parameters) {
        walkType(param.type);
      }
    }

    return external;
  }

  /// Generates an opaque stub class for a nativeObject type.
  void _generateNativeObjectStub(StringBuffer buffer, String typeName) {
    buffer.writeln('/// Opaque handle to a native $typeName object.');
    buffer.writeln('class $typeName {');
    buffer.writeln('  final String _handle;');
    buffer.writeln('  $typeName._(this._handle);');
    buffer.writeln(
        '  static $typeName fromHandle(String handle) => $typeName._(handle);');
    buffer.writeln('}');
  }

  /// Returns the set of nativeObject type names that were auto-stubbed
  /// (not user-provided via customTypes).
  Set<String> collectUnresolvedTypes(UnifiedTypeSchema schema,
      {Map<String, String> customTypes = const {}}) {
    final external = _collectExternalNativeObjectTypes(schema);
    external.removeAll(customTypes.keys);
    return external;
  }

  String _packageToFileName(String packageName) {
    return '${_toSnakeCase(packageName)}.dart';
  }

  String _packageToClassName(String packageName) {
    return _toPascalCase(packageName);
  }

  String _packageToChannelName(String packageName) {
    return _toSnakeCase(packageName);
  }

  static String _toSnakeCase(String input) {
    return input
        .replaceAll(RegExp(r'[^a-zA-Z0-9]'), '_')
        .replaceAll(RegExp(r'_+'), '_')
        .replaceAll(RegExp(r'^_|_$'), '')
        .toLowerCase();
  }

  static String _toPascalCase(String input) {
    return input
        .replaceAll(RegExp(r'[^a-zA-Z0-9]'), ' ')
        .split(RegExp(r'\s+'))
        .where((s) => s.isNotEmpty)
        .map((s) => s[0].toUpperCase() + s.substring(1).toLowerCase())
        .join();
  }

  static String _platformAvailability(PackageSource source) {
    switch (source) {
      case PackageSource.npm:
        return 'Web only (npm package)';
      case PackageSource.cocoapods:
        return 'iOS and macOS only (CocoaPods package)';
      case PackageSource.spm:
        return 'iOS and macOS only (Swift Package Manager)';
      case PackageSource.gradle:
        return 'Android only (Gradle/Maven package)';
    }
  }
